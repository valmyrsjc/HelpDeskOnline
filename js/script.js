/* funcão para o div conteudoChamados */

function showChamados() {
	if (document.getElementById('conteudoChamados').style.display == 'none') {
		document.getElementById('conteudoChamados').style.display = 'block';
	}
	return false;
}

function hideChamados() {
	if (document.getElementById('conteudoChamados').style.display == 'block') {
		document.getElementById('conteudoChamados').style.display = 'none';
	}
	return false;
}

